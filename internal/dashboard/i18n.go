func resolveLang(r *http.Request) string {
	lang := r.URL.Query().Get("lang")
	if lang == "" {
		if cookie, err := r.Cookie("vivurouter_lang"); err == nil {
			lang = cookie.Value
		}
	}
	if lang != "vi" && lang != "en" {
		lang = "vi"
	}
	return lang
}

func translationBundle(lang string) map[string]string {
	bundle, ok := translations[lang]
	if !ok {
		bundle = translations["vi"]
	}
	return bundle
}
